<h1 class="alt">POLICIES</h1>
<p>Below is some important information about our practice. Please review this in advance of your appointment.</p>
<h3>Hours</h3>
<p>Our hours are Monday – Friday, 8:00 AM – 5:00 PM (Closed for lunch from 12:00PM to 1:00PM). We will soon be adding an audiologist to our practice to assist in all audiological exam needs.</p>
<h3>Appointments</h3>
<p>Appointments can be anytime during normal business hours Monday through Friday.

  We will be closed for the following holidays:</p>
<ul class="servicesUL">
<li>Memorial Day</li>
<li>4th of July</li>
<li>Thanksgiving</li>
<li>Christmas</li>
<li>New Years Day</li>
</ul>
<h3>Same Day/Walk Ins</h3>
<p> If you need to see us immediately, please call our office and explain your issue to our nurse, who will work with you to schedule an appointment for you as quickly as possible. Walk-in appointments are not accepted.<br />
</p>
<h3>Cancellations</h3>
<p>If an appointment must be canceled, then please notify us as soon as possible. No shows and cancellations within 24 hours will  be charged a fee of $25 regardless of the reason. Informing us of your cancellation simply allows us to see other patients that may need to be worked in and opens up other scheduling options for patients.

  <br />
  <br />
  If you are running late to the appointment, then please let us know so we can adjust our schedule accordingly.  </p>
<h3>Prescription Refills  </h3>
<p>The best time to obtain prescription refills is during your appointment. Otherwise, the best way to obtain refill requests is to call your pharmacy and they will contact us with all the pertinent information we need. We will do our very best to refill your prescriptions as soon as possible and most prescriptions are refilled within 24 hours. Like most physician offices, there are times in which we will request an office visit before refilling a prescription. However, we will gladly supply you a prescription for enough of your medication to last you until we can see you in the office. The on-call physician will not refill medications after regular office hours.  </p>
<h3>Insurance  </h3>
<p>Always check with your insurance company before selecting a physician. We accept the following insurance plans:<br />
  <ul class="servicesUL">
<li>Most private insurance (e.g. Blue Cross Blue Shield, United Health Care, Medcost)</li>
<li>Medicare</li>
<li>Medicaid</li>
<li>Tricare Prime and Tricare Standard (with a referral from your primary care doctor.)</li>
</ul>
<h3>Privacy Policy and Patients Rights</h3>
<p>As a patient, you are a partner in your care. Consequently you have rights and responsibilities. Click <a href="forms/OPC Patient Rights and Responsibilities.pdf">here</a> to download printable versions of our Patient Rights and Responsibilities form. You can also download our Privacy Policy by clicking <a href="forms/Privacy-Policy.pdf">here</a>.</p>
